public class number_triangle {
    public static void main(String[] args){

        int n=4;
        int nol =1;
        
        while(nol <= n){

            for(int i=1; i<=nol;i++){
                System.out.print(i + " ");
            }

            System.out.println();
            nol ++;
        }
    }
    
}
